package com.shxt.cme.domain;

public class InUnit {
	
	private String inUnitKey;  //所在单位主键
	private String inUnitName;  //所在单位名称
	private String inUnitCode;  //所在单位排序编码
	public String getInUnitKey() {
		return inUnitKey;
	}
	public void setInUnitKey(String inUnitKey) {
		this.inUnitKey = inUnitKey;
	}
	public String getInUnitName() {
		return inUnitName;
	}
	public void setInUnitName(String inUnitName) {
		this.inUnitName = inUnitName;
	}
	public String getInUnitCode() {
		return inUnitCode;
	}
	public void setInUnitCode(String inUnitCode) {
		this.inUnitCode = inUnitCode;
	}

}
