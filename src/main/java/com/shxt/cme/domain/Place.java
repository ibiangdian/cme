package com.shxt.cme.domain;

public class Place {

	private String placeKey;
	private String placeName;
	private String placeLevel;
	private String placeDataCode;
	private String placeSortCode;
	private String upPlaceKey;
	public String getPlaceKey() {
		return placeKey;
	}
	public void setPlaceKey(String placeKey) {
		this.placeKey = placeKey;
	}
	public String getPlaceName() {
		return placeName;
	}
	public void setPlaceName(String placeName) {
		this.placeName = placeName;
	}
	public String getPlaceLevel() {
		return placeLevel;
	}
	public void setPlaceLevel(String placeLevel) {
		this.placeLevel = placeLevel;
	}

	public String getPlaceDataCode() {
		return placeDataCode;
	}
	public void setPlaceDataCode(String placeDataCode) {
		this.placeDataCode = placeDataCode;
	}
	public String getPlaceSortCode() {
		return placeSortCode;
	}
	public void setPlaceSortCode(String placeSortCode) {
		this.placeSortCode = placeSortCode;
	}
	public String getUpPlaceKey() {
		return upPlaceKey;
	}
	public void setUpPlaceKey(String upPlaceKey) {
		this.upPlaceKey = upPlaceKey;
	}
	
}
